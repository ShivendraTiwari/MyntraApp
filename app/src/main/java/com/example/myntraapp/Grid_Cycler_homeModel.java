package com.example.myntraapp;

public class Grid_Cycler_homeModel {

    public int image;
    public String text;

    public Grid_Cycler_homeModel(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
